def docker_run(*a, **kw):
    return subprocess.run(["docker"] + list(a), capture_output=True, text=True, **kw)


def setup_docker_target() -> None:
    print("  setup_docker_target: rm prior...", flush=True)
    subprocess.run(["docker", "rm", "-f", CONTAINER], capture_output=True, timeout=30)
    print("  setup_docker_target: starting container...", flush=True)
    rc = subprocess.call([
        "docker", "run", "-d", "--name", CONTAINER,
        "-v", "/usr/lib/dotnet:/usr/lib/dotnet:ro",
        "--entrypoint", "sleep", "netcore-lldb:dev", "infinity"
    ], stdout=subprocess.DEVNULL, timeout=60)
    if rc != 0:
        raise SystemExit("could not start docker target container")
    print("  setup_docker_target: copying dump...", flush=True)
    subprocess.run(["docker", "cp", DUMP_LOCAL, f"{CONTAINER}:/work/dump.core"], check=True, timeout=60)
    print("  setup_docker_target: mkdir app...", flush=True)
    subprocess.run(["docker", "exec", CONTAINER, "mkdir", "-p", "/work/app"], check=True, timeout=30)
    print("  setup_docker_target: copying app dir...", flush=True)
    subprocess.run(["docker", "cp", f"{APP_LOCAL}/.", f"{CONTAINER}:/work/app/"], check=True, timeout=60)
    # Symlink the dump's recorded exe path to /work/app so lldb auto-loads it.
    print("  setup_docker_target: setting up dump-recorded-path symlinks...", flush=True)
    subprocess.run([
        "docker", "exec", CONTAINER, "bash", "-c",
        'RECORDED=$(strings /work/dump.core | grep -oE "^/[^[:space:]]+/Crasher$" | head -1 || true); '
        '[ -n "$RECORDED" ] && mkdir -p "$(dirname $RECORDED)" && '
        'for f in /work/app/*; do ln -sf "$f" "$(dirname $RECORDED)/$(basename $f)"; done'
    ], check=False, timeout=30)
    print("  setup_docker_target: done.", flush=True)


def setup_ssh_target() -> None:
    # The ssh target is WSL itself (cad@127.0.0.1). DUMP_LOCAL on the
    # Windows-mounted filesystem is reachable directly from WSL — the dump's
    # recorded paths (/mnt/c/.../sample/out/Crasher) ARE valid on WSL because
    # /mnt/c is the Windows mount. We stage copies under /tmp/netcore-lldb-test/
    # for the client to point at, but we deliberately DO NOT create symlinks
    # at the dump-recorded path because that would clobber the real source dir.
    print("  setup_ssh_target: sshing in to stage dump and app...", flush=True)
    ssh_cmd = [
        "ssh", "-o", "BatchMode=yes", "-o", "ConnectTimeout=5",
        "-i", "/home/cad/.ssh/netcore-lldb-test_ed25519",
        SSH_HOST,
        'rm -rf /tmp/netcore-lldb-test && mkdir -p /tmp/netcore-lldb-test/app && '
        'cp -r ' + APP_LOCAL + '/. /tmp/netcore-lldb-test/app/ && '
        'cp ' + DUMP_LOCAL + ' /tmp/netcore-lldb-test/dump.core'
    ]
    try:
        subprocess.run(ssh_cmd, check=False, timeout=60, capture_output=True)
        print("  setup_ssh_target: done.", flush=True)
    except subprocess.TimeoutExpired:
        print("  setup_ssh_target: SSH staging TIMED OUT — ssh tests will fail", flush=True)


