    responses, _, _ = drive(args, msgs, timeout=120)
    r = find_response(responses, 2)
    text = text_of(r) if r else ""
    m = re.search(r"Total\s+(\d+)\s+objects", text)
    count = int(m.group(1)) if m else 0
    # Each Publisher.Tick += sub.OnTick allocates an EventHandler delegate.
    # 100 subscriptions → at least ~100 EventHandler instances (often more
    # because each += clones the multicast list).
    ok = count >= 100
    record(name, ok, f"EventHandler instances on heap: {count}")


