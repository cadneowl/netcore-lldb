results: list[tuple[str, bool, str]] = []


def msg(method: str, **params) -> dict:
    m = {"jsonrpc": "2.0", "method": method, "id": params.pop("_id", 1)}
    if params:
        m["params"] = params
    return m


def notif(method: str, **params) -> dict:
    m = {"jsonrpc": "2.0", "method": method}
    if params:
        m["params"] = params
    return m


def initialize_seq() -> list[dict]:
    return [
        {"jsonrpc": "2.0", "id": 1, "method": "initialize",
         "params": {"protocolVersion": "2024-11-05", "capabilities": {}, "clientInfo": {"name": "t", "version": "0"}}},
        {"jsonrpc": "2.0", "method": "notifications/initialized"},
    ]


def drive(args: list[str], messages: list[dict], timeout: float = 90.0) -> tuple[list[dict], str, int]:
    """Spawn the client, send messages, return (responses, stderr, rc)."""
    proc = subprocess.Popen(
        [sys.executable, CLIENT] + args,
        stdin=subprocess.PIPE, stdout=subprocess.PIPE, stderr=subprocess.PIPE,
        text=True,
    )
    inp = "\n".join(json.dumps(m) for m in messages) + "\n"
    try:
        out, err = proc.communicate(input=inp, timeout=timeout)
    except subprocess.TimeoutExpired:
        proc.kill()
        out, err = proc.communicate()
        return [], err, -1
    responses = [json.loads(ln) for ln in out.splitlines() if ln.strip()]
    return responses, err, proc.returncode


def find_response(responses: list[dict], msg_id) -> dict | None:
    for r in responses:
        if r.get("id") == msg_id:
            return r
    return None


def text_of(resp: dict) -> str:
    return resp.get("result", {}).get("content", [{}])[0].get("text", "")


def record(name: str, ok: bool, note: str = "") -> None:
    results.append((name, ok, note))
    mark = "PASS" if ok else "FAIL"
    color = "\033[32m" if ok else "\033[31m"
    print(f"  {color}{mark}\033[0m  {name}{(' — ' + note) if note else ''}")


