#!/usr/bin/env python3
"""
End-to-end test suite for the netcore-lldb client tool.

Runs the client against two real targets:
  1. A docker container (started fresh, has lldb + SOS + the dump)
  2. SSH to localhost (WSL has lldb + SOS; the dump is on the WSL filesystem)

For each, the suite exercises:
  - argument parsing / mutually-exclusive transports
  - preflight checks (unreachable target, missing lldb, missing dump)
  - MCP protocol (initialize, tools/list, ping, shutdown, unknown method)
  - tool dispatch (unknown tool, bad arguments, normal call)
  - SOS command coverage (eeversion, clrthreads, pe, clrstack with source lines,
    dumpheap -stat, clrmodules)
  - stateful session (thread select, then clrstack on that thread)
  - --copy-dump (pushes a local dump to the target before opening lldb)

Test names are tagged [docker] / [ssh] / [common].

Exit code: 0 if all pass, 1 if any fail. Prints a summary at the end.
"""

