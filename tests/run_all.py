def test_bootstrap_installs_lldb_and_sos_on_bare_target():
    """The real customer scenario: their app's container has the .NET runtime
    but not lldb. Verify that `--bootstrap` installs both lldb and the SOS
    debugger extension into a fresh container, and that SOS commands work
    immediately afterward.

    Takes 1-3 minutes the first time (apt-get install + dotnet tool install).
    Re-running against an already-bootstrapped target is fast (idempotent)."""
    name = "[docker/bootstrap] --bootstrap installs lldb+SOS on a target that lacks them"
    bare = "netcore-lldb-bootstrap-test"
    docker_run("rm", "-f", bare)
    rc = subprocess.call([
        "docker", "run", "-d", "--name", bare,
        # Mount the WSL host's actual dotnet install so SOS finds byte-identical
        # runtime binaries when analyzing this fixture's dump. In production the
        # target IS the customer's container, so this match is automatic.
        "-v", "/usr/lib/dotnet:/usr/lib/dotnet:ro",
        "--entrypoint", "sleep",
        "mcr.microsoft.com/dotnet/sdk:10.0-noble", "infinity",
    ], stdout=subprocess.DEVNULL, timeout=60)
    if rc != 0:
        record(name, False, "could not start bare dotnet SDK target")
        return
    try:
        # Symlink the alternate dotnet install path the dump records into the
        # one Docker resolved the bind mount to.
        subprocess.run(["docker", "exec", bare, "sh", "-c",
                        "[ -e /usr/lib/dotnet ] || ln -sf /usr/share/dotnet /usr/lib/dotnet"],
                       capture_output=True, timeout=15)
        # Sanity: confirm lldb is NOT in the bare image before --bootstrap.
        rc, out, _ = (lambda r: (r.returncode, r.stdout, r.stderr))(
            subprocess.run(["docker", "exec", bare, "sh", "-c", "command -v lldb || echo MISSING"],
                           capture_output=True, text=True, timeout=10))
        if "MISSING" not in out:
            record(name, False, f"bare image already had lldb? out={out!r}")
            return

        # Stage the crasher dump + binaries inside the target.
        # Create /work first — the bare SDK image doesn't have it (only our
        # reference image does, via its Dockerfile).
        subprocess.run(["docker", "exec", bare, "mkdir", "-p", "/work/app"],
                       check=True, timeout=15)
        subprocess.run(["docker", "cp", DUMP_LOCAL, f"{bare}:/work/dump.core"],
                       check=True, timeout=60)
        subprocess.run(["docker", "cp", f"{APP_LOCAL}/.", f"{bare}:/work/app/"],
                       check=True, timeout=60)
        # Make the dump's recorded executable path resolve to /work/app/Crasher.
        subprocess.run(["docker", "exec", bare, "bash", "-c",
                        'RECORDED=$(strings /work/dump.core 2>/dev/null | grep -oE "^/[^[:space:]]+/Crasher$" | head -1 || true); '
                        '[ -n "$RECORDED" ] && mkdir -p "$(dirname $RECORDED)" && '
                        'for f in /work/app/*; do ln -sf "$f" "$(dirname $RECORDED)/$(basename $f)"; done'],
                       capture_output=True, timeout=15)

        # Drive the client with --bootstrap and a SOS query in one go.
        args = ["--docker", bare, "--bootstrap",
                "--dump", "/work/dump.core", "--exec", "/work/app/Crasher"]
        msgs = initialize_seq() + [
            {"jsonrpc": "2.0", "id": 2, "method": "tools/call",
             "params": {"name": "lldb_command", "arguments": {"command": "eeversion"}}},
            {"jsonrpc": "2.0", "id": 3, "method": "tools/call",
             "params": {"name": "lldb_command", "arguments": {"command": "pe"}}},
        ]
        responses, err, _ = drive(args, msgs, timeout=600)
        eev = find_response(responses, 2)
        pe = find_response(responses, 3)
        eev_text = text_of(eev) if eev else ""
        pe_text = text_of(pe) if pe else ""
        ok = ("SOS Version" in eev_text
              and "InvalidOperationException" in pe_text
              and "mandatoryTag must not be null" in pe_text)
        note = (f"eeversion sos-ok={'SOS Version' in eev_text}, "
                f"pe matched={'mandatoryTag' in pe_text}, "
                f"stderr tail: {err[-200:].strip() if err else ''}")
        record(name, ok, note)
    finally:
        docker_run("rm", "-f", bare)


